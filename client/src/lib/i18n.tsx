type I18nContextValue = {
  market: MarketConfig;
  markets: MarketConfig[];
  setMarketCode: (code: MarketCode) => void;
  t: (key: string, variables?: Record<string, string | number>) => string;
  formatCurrency: (value: number | string) => string;
  formatNumber: (value: number | string) => string;
  formatDateTime: (value: string | number | Date) => string;
  isRTL: boolean;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function interpolate(template: string, variables?: Record<string, string | number>) {
  if (!variables) return template;
  return template.replace(/\{(\w+)\}/g, (_match, key: string) => String(variables[key] ?? ""));
}

function inferInitialMarket(): MarketCode {
  if (typeof window === "undefined") return "US";

  const stored = window.localStorage.getItem(STORAGE_KEY) as MarketCode | null;
  if (stored && stored in MARKETS) return stored;

  const language = window.navigator.language.toLowerCase();
  if (language.startsWith("fr")) return "FR";
  if (language.startsWith("ar")) return "AE";
  return "US";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [marketCode, setMarketCode] = useState<MarketCode>(() => inferInitialMarket());
  const market = MARKETS[marketCode];

  useEffect(() => {
    window.localStorage.setItem(STORAGE_KEY, marketCode);
    document.documentElement.lang = market.locale;
    document.documentElement.dir = market.direction;
  }, [market, marketCode]);

  const value = useMemo<I18nContextValue>(() => ({
    market,
    markets: Object.values(MARKETS),
    setMarketCode,
    t: (key, variables) => {
      const dictionary = translations[market.language] ?? translations.en;
      const fallback = translations.en[key] ?? key;
      return interpolate(dictionary[key] ?? fallback, variables);
    },
    formatCurrency: (value) =>
      new Intl.NumberFormat(market.locale, {
        style: "currency",
        currency: market.currency,
        maximumFractionDigits: market.currency === "RWF" ? 0 : 2,
      }).format(Number(value) || 0),
    formatNumber: (value) =>
      new Intl.NumberFormat(market.locale, {
        maximumFractionDigits: 1,
      }).format(Number(value) || 0),
    formatDateTime: (value) =>
      new Intl.DateTimeFormat(market.locale, {
        dateStyle: "medium",
        timeStyle: "short",
      }).format(new Date(value)),
    isRTL: market.direction === "rtl",
  }), [market, marketCode]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}
